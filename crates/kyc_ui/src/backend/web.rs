// ── Style Generator ──────────────────────────────────────────────

fn generate_styles(styles: &[StyleDecl]) -> String {
    let mut js = String::new();

    let mut has_media = false;
    js.push_str("const _style_data = {\n");
    for decl in styles {
        match decl {
            StyleDecl::Style { name, props, media, .. }
            | StyleDecl::Layout { name, props, media, .. }
            | StyleDecl::Template { name, props, media, .. } => {
                js.push_str(&format!("  '{}': {{\n", name));
                for prop in props {
                    let css_prop = to_css_name(&prop.name);
                    let css_val = to_css_value(&prop.name, &prop.value);
                    js.push_str(&format!("    '{}': '{}',\n", css_prop, css_val));
                }
                for mq in media {
                    has_media = true;
                    let mq_cond = to_media_condition(&mq.condition);
                    js.push_str(&format!("    '{}': {{\n", mq_cond));
                    for prop in &mq.props {
                        let css_prop = to_css_name(&prop.name);
                        let css_val = to_css_value(&prop.name, &prop.value);
                        js.push_str(&format!("      '{}': '{}',\n", css_prop, css_val));
                    }
                    js.push_str("    },\n");
                }
                js.push_str("  },\n");
            }
            StyleDecl::Theme { name, props } => {
                js.push_str(&format!("  'theme:{}': {{\n", name));
                for prop in props {
                    js.push_str(&format!("    '{}': '{}',\n", prop.name, prop.value));
                }
                js.push_str("  },\n");
            }
        }
    }
    js.push_str("};\n\n");

    // Export styles
    js.push_str("export const styles = _style_data;\n\n");

    js.push_str("export function applyStyle(el, styleName) {\n");
    js.push_str("  const s = _style_data[styleName];\n");
    js.push_str("  if (!s) return;\n");
    js.push_str("  const base = {};\n");
    js.push_str("  const mediaQueries = {};\n");
    js.push_str("  for (const [key, val] of Object.entries(s)) {\n");
    js.push_str("    if (key.startsWith('@media')) {\n");
    js.push_str("      mediaQueries[key] = val;\n");
    js.push_str("    } else {\n");
    js.push_str("      base[key] = val;\n");
    js.push_str("    }\n");
    js.push_str("  }\n");
    js.push_str("  for (const [prop, val] of Object.entries(base)) {\n");
    js.push_str("    el.style[prop] = val;\n");
    js.push_str("  }\n");
    js.push_str("  for (const [mq, rules] of Object.entries(mediaQueries)) {\n");
    js.push_str("    const mql = window.matchMedia(mq.replace('@media ', ''));\n");
    js.push_str("    const apply = () => {\n");
    js.push_str("      if (mql.matches) {\n");
    js.push_str("        for (const [prop, val] of Object.entries(rules)) {\n");
    js.push_str("          el.style[prop] = val;\n");
    js.push_str("        }\n");
    js.push_str("      }\n");
    js.push_str("    };\n");
    js.push_str("    apply();\n");
    js.push_str("    mql.addEventListener('change', apply);\n");
    js.push_str("  }\n");
    js.push_str("}\n\n");

    js
}

