use crate::ir::*;
use super::{GeneratedFile, BackendOutput, UiBackend};
use std::sync::atomic::{AtomicUsize, Ordering};

static EL_COUNTER: AtomicUsize = AtomicUsize::new(0);

fn fresh_el() -> String {
    let id = EL_COUNTER.fetch_add(1, Ordering::Relaxed);
    format!("_el{}", id)
}

pub struct WebBackend;

impl WebBackend {
    pub fn new() -> Self { Self }
}

impl UiBackend for WebBackend {
    fn name(&self) -> &str {
        "web"
    }

    fn target_triple(&self) -> &str {
        "wasm32-unknown-unknown"
    }

    fn generate(&self, program: &UiProgram) -> BackendOutput {
        let mut js = String::new();

        // Import ESM runtimes with absolute paths from server root
        js.push_str("// Generated by Kyle UI Compiler\n");
        js.push_str("// Target: Web (JS/DOM) — ES Modules\n\n");

        js.push_str("import { ReactiveState, Binding, createKyleEvent } from '/target/debug/reactivity.js';\n");
        js.push_str("import { Router, routeParams, set_title, set_meta, navigate, navigate_back, navigate_replace } from '/target/debug/router.js';\n");
        js.push_str("import { A11yManager } from '/target/debug/a11y.js';\n");
        js.push_str("import { portalManager } from '/target/debug/portal.js';\n");
        js.push_str("import { ErrorBoundary } from '/target/debug/error_boundary.js';\n");
        js.push_str("import { getI18n, t, setLocale } from '/target/debug/i18n.js';\n");
        js.push_str("const _i18n = getI18n();\n");
        js.push_str("const _a11y = new A11yManager();\n\n");

        // Generate styles
        let style_js = generate_styles(&program.styles);
        js.push_str(&style_js);

        // Generate animations
        let anim_js = generate_animations(&program.animations);
        js.push_str(&anim_js);

        // Determine component name from view path — always export as 'render'
        let comp_name = "render".to_string();

        // Generate component function (exported at end)
        js.push_str("function render(container, initialState = {}) {\n");
        js.push_str("  const state = new ReactiveState(initialState);\n");

        // Translate code blocks (@(...)) to JS
        for block in &program.code_blocks {
            let js_block = translate_kyle_block_to_js(block);
            if !js_block.is_empty() {
                js.push_str("  // From Kyle block:\n");
                for line in js_block.lines() {
                    js.push_str(&format!("  {}\n", line));
                }
            }
        }

        js.push_str("  const el = document.createDocumentFragment();\n");

        for node in &program.body {
            gen_node(node, &mut js, 2, "el", "");
        }

        js.push_str("  return { element: el, state };\n");
        js.push_str("}\n\n");

        // Generate component renderers from resolved dependencies
        for comp in &program.component_renderers {
            let comp_name = &comp.name;
            js.push_str(&format!("\n// Component renderer: {}\n", comp_name));
            js.push_str(&format!("function render_{}(params = {{}}) {{\n", comp_name));
            js.push_str("  const state = new ReactiveState({});\n");

            // Code blocks for this component
            for block in &comp.code_blocks {
                let js_block = translate_kyle_block_to_js(block);
                if !js_block.is_empty() {
                    js.push_str("  // From Kyle block:\n");
                    for line in js_block.lines() {
                        js.push_str(&format!("  {}\n", line));
                    }
                }
            }

            js.push_str("  const el = document.createDocumentFragment();\n");
            for node in &comp.body {
                gen_node(node, &mut js, 2, "el", "");
            }
            js.push_str("  return { element: el, state };\n");
            js.push_str("}\n");
        }

        // Route registration from RouteConfig
        if !program.routes.is_empty() {
            js.push_str("\n// Register and start router\n");
            js.push_str("if (typeof window !== 'undefined') {\n");
            js.push_str("  if (!window.__KYLE_ROUTES) window.__KYLE_ROUTES = {};\n");
            for route in &program.routes {
                // Use component-specific renderer if available
                let comp_name = &route.component;
                let func_name = format!("render_{}", comp_name);
                // Check if a component renderer exists with this name
                let has_renderer = program.component_renderers.iter()
                    .any(|c| c.name == *comp_name);
                if has_renderer {
                    js.push_str(&format!("  window.__KYLE_ROUTES[{:?}] = {};\n", route.path, func_name));
                } else {
                    js.push_str(&format!("  window.__KYLE_ROUTES[{:?}] = render;\n", route.path));
                }
            }
            // Create and start router
            js.push_str("  const router = createRouter({ container: document.getElementById('app') });\n");
            js.push_str("  router.start();\n");
            js.push_str("}\n\n");
        }

        // Export render function
        js.push_str("export { render };\n");

        // Build HTML shell (adapts if routes exist — router handles rendering)
        let has_routes = !program.routes.is_empty();
        let html = generate_html(!has_routes);

        BackendOutput {
            files: vec![GeneratedFile {
                path: format!("ui-runtime.js"),
                content: js,
            }],
            html_shell: Some(html),
        }
    }
}

fn gen_node(node: &UiNode, js: &mut String, indent: usize, parent: &str, model: &str) {
    let el = fresh_el();
    let ind = " ".repeat(indent);
    let ind2 = " ".repeat(indent + 2);

    match node {
        UiNode::Element { tag, attrs, children } => {
            // Detect form model context
            let mut child_model = model;
            if tag == &ComponentTag::Form {
                for a in attrs {
                    if a.name == "model" {
                        if let AttrValue::Expr(ref v) = a.value {
                            child_model = v.trim_start_matches('@');
                        }
                    }
                }
            }

            let tag_js = js_tag(tag);
            if tag == &ComponentTag::FilePicker {
                let mut accept = String::new();
                let mut multiple = false;
                let mut on_select = String::new();
                let mut field = String::new();
                for a in attrs {
                    match a.name.as_str() {
                        "accept" => if let AttrValue::String(ref v) = a.value { accept = v.clone(); },
                        "multiple" => multiple = true,
                        "on_select" => if let AttrValue::Expr(ref v) = a.value { on_select = v.clone(); },
                        "field" => if let AttrValue::String(ref v) = a.value { field = v.clone(); },
                        _ => {}
                    }
                }
                // If field= is set and there's a model, auto-generate the callback
                let single_callback = if !field.is_empty() && !child_model.is_empty() {
                    format!("(fileData) => {{ state['{}'] = {{ ...(state['{}'] || {{}}), '{}': fileData }}; }}", child_model, child_model, field)
                } else if !on_select.is_empty() {
                    format!("({})", on_select.clone())
                } else {
                    String::new()
                };
                let multi_callback = if !field.is_empty() && !child_model.is_empty() {
                    format!("(files) => {{ state['{}'] = {{ ...(state['{}'] || {{}}), '{}': files }}; }}", child_model, child_model, field)
                } else if !on_select.is_empty() {
                    format!("({})", on_select.clone())
                } else {
                    String::new()
                };
                // Generate file input + handler
                let input_el = fresh_el();
                js.push_str(&format!("{}// FilePicker\n", ind));
                js.push_str(&format!("{}const {} = document.createElement('input');\n", ind, input_el));
                js.push_str(&format!("{}{}.type = 'file';\n", ind, input_el));
                if !accept.is_empty() {
                    js.push_str(&format!("{}{}.accept = {:?};\n", ind, input_el, accept));
                }
                if multiple {
                    js.push_str(&format!("{}{}.multiple = true;\n", ind, input_el));
                }
                js.push_str(&format!("{}{}.style.display = 'none';\n", ind, input_el));
                // Click handler triggers file picker
                js.push_str(&format!("{}{}.addEventListener('change', async () => {{\n", ind, input_el));
                if multiple {
                    js.push_str(&format!("{}  const files = [];\n", ind));
                    js.push_str(&format!("{}  for (const f of {}.files) {{\n", ind, input_el));
                    js.push_str(&format!("{}    const buf = await f.arrayBuffer();\n", ind));
                    js.push_str(&format!("{}    files.push({{ name: f.name, size: f.size, mime: f.type, content: new Uint8Array(buf), last_modified: f.lastModified }});\n", ind));
                    js.push_str(&format!("{}  }}\n", ind));
                    if !multi_callback.is_empty() {
                        js.push_str(&format!("{}  ({})(files);\n", ind, multi_callback));
                    }
                } else {
                    js.push_str(&format!("{}  const f = {}.files[0];\n", ind, input_el));
                    js.push_str(&format!("{}  if (!f) return;\n", ind));
                    js.push_str(&format!("{}  const buf = await f.arrayBuffer();\n", ind));
                    js.push_str(&format!("{}  const fileData = {{ name: f.name, size: f.size, mime: f.type, content: new Uint8Array(buf), last_modified: f.lastModified }};\n", ind));
                    if !single_callback.is_empty() {
                        js.push_str(&format!("{}  ({})(fileData);\n", ind, single_callback));
                    }
                }
                js.push_str(&format!("{}}});\n", ind));
                // Trigger click on mount
                js.push_str(&format!("{}{}.click();\n", ind, input_el));
                js.push_str(&format!("{}{}.appendChild({});\n", ind, parent, input_el));
                return;
            }

            if tag == &ComponentTag::Portal {
                let mut target = "body";
                for a in attrs {
                    if a.name == "target" {
                        if let AttrValue::String(ref v) = a.value { target = v; }
                    }
                }
                js.push_str(&format!("{}// Portal: render to '{}'\n", ind, target));
                for child in children {
                    gen_node(child, js, indent, &format!("{}_content", el), model);
                }
                js.push_str(&format!("{}if (typeof {}_content !== 'undefined') {{\n", ind, el));
                js.push_str(&format!("{}  portalManager.create({}_content, {:?});\n", ind, el, target));
                js.push_str(&format!("{}}}\n", ind));
                return;
            }
            if tag == &ComponentTag::ErrorBoundary {
                let b = fresh_el();
                js.push_str(&format!("{}// Error boundary\n", ind));
                js.push_str(&format!("{}const {} = new ErrorBoundary();\n", ind, b));
                js.push_str(&format!("{}const {}_wrap = {}.wrap(() => {{\n", ind, b, b));
                js.push_str(&format!("{}  const {}_c = document.createDocumentFragment();\n", ind, b));
                for child in children {
                    gen_node(child, js, indent + 2, &format!("{}_c", b), model);
                }
                js.push_str(&format!("{}  return {}_c;\n", ind, b));
                js.push_str(&format!("{}}});\n", ind));
                js.push_str(&format!("{}const {}_r = {}_wrap();\n", ind, b, b));
                js.push_str(&format!("{}if ({}_r) {{ {}.appendChild({}_r); }}\n", ind, b, parent, b));
                return;
            }
            if tag == &ComponentTag::Slot {
                let mut name = "default";
                for a in attrs {
                    if a.name == "name" {
                        if let AttrValue::String(ref v) = a.value { name = v; }
                    }
                }
                js.push_str(&format!("{}// Outlet: '{}'\n", ind, name));
                js.push_str(&format!("{}portalManager.registerOutlet({:?}, {});\n", ind, name, el));
            }
            js.push_str(&format!("{}const {} = document.createElement('{}');\n", ind, el, tag_js));
            gen_attrs(attrs, js, indent, &el);
            gen_events(attrs, js, indent, &el, child_model);

            // App component: handle title and icon
            if tag == &ComponentTag::App {
                for a in attrs {
                    if a.name == "title" {
                        let val = match &a.value {
                            AttrValue::String(v) => format!("{:?}", v),
                            AttrValue::Expr(v) => v.clone(),
                            AttrValue::Flag => String::new(),
                        };
                        if !val.is_empty() {
                            js.push_str(&format!("{}document.title = {};\n", ind, val));
                        }
                    }
                    if a.name == "icon" {
                        let val = match &a.value {
                            AttrValue::String(v) => format!("{:?}", v),
                            AttrValue::Expr(v) => v.clone(),
                            AttrValue::Flag => String::new(),
                        };
                        if !val.is_empty() {
                            js.push_str(&format!("{}let _icon = document.querySelector('link[rel*=\"icon\"]'); if (!_icon) {{ _icon = document.createElement('link'); _icon.rel = 'icon'; document.head.appendChild(_icon); }} _icon.href = {};\n", ind, val));
                        }
                    }
                }
            }

            // Link component: intercept clicks for client-side navigation
            if tag == &ComponentTag::Link {
                for a in attrs {
                    if a.name == "to" {
                        let target = match &a.value {
                            AttrValue::String(v) => format!("{:?}", v),
                            AttrValue::Expr(v) => v.clone(),
                            AttrValue::Flag => String::new(),
                        };
                        js.push_str(&format!("{}{}.addEventListener('click', (e) => {{ e.preventDefault(); navigate({}); }});\n", ind, el, target));
                    }
                }
            }

            js.push_str(&format!("{}A11yManager.applyAria({}, {:?});\n", ind, el, tag.as_str()));
            for child in children {
                gen_node(child, js, indent + 2, &el, child_model);
            }
            js.push_str(&format!("{}{}.appendChild({});\n", ind, parent, el));
        }
        UiNode::SelfClosing { tag, attrs } => {
            // FilePicker: generate hidden file input with FileReader
            if tag == &ComponentTag::FilePicker {
                let mut accept = String::new();
                let mut multiple = false;
                let mut on_select = String::new();
                let mut field = String::new();
                for a in attrs {
                    match a.name.as_str() {
                        "accept" => if let AttrValue::String(ref v) = a.value { accept = v.clone(); },
                        "multiple" => multiple = true,
                        "on_select" => if let AttrValue::Expr(ref v) = a.value { on_select = v.clone(); },
                        "field" => if let AttrValue::String(ref v) = a.value { field = v.clone(); },
                        _ => {}
                    }
                }
                let single_callback = if !field.is_empty() && !model.is_empty() {
                    format!("(fileData) => {{ state['{}'] = {{ ...(state['{}'] || {{}}), '{}': fileData }}; }}", model, model, field)
                } else if !on_select.is_empty() {
                    format!("({})", on_select.clone())
                } else {
                    String::new()
                };
                let mut multi_callback = if !field.is_empty() && !model.is_empty() {
                    format!("(files) => {{ state['{}'] = {{ ...(state['{}'] || {{}}), '{}': files }}; }}", model, model, field)
                } else if !on_select.is_empty() {
                    format!("({})", on_select.clone())
                } else {
                    String::new()
                };
                // For multi on_select, use the same callback name
                if multi_callback.is_empty() && !on_select.is_empty() {
                    multi_callback = on_select.clone();
                }
                let input_el = fresh_el();
                js.push_str(&format!("{}// FilePicker\n", ind));
                js.push_str(&format!("{}const {} = document.createElement('input');\n", ind, input_el));
                js.push_str(&format!("{}{}.type = 'file';\n", ind, input_el));
                if !accept.is_empty() {
                    js.push_str(&format!("{}{}.accept = {:?};\n", ind, input_el, accept));
                }
                if multiple {
                    js.push_str(&format!("{}{}.multiple = true;\n", ind, input_el));
                }
                js.push_str(&format!("{}{}.style.display = 'none';\n", ind, input_el));
                js.push_str(&format!("{}{}.addEventListener('change', async () => {{\n", ind, input_el));
                if multiple {
                    js.push_str(&format!("{}  const files = [];\n", ind));
                    js.push_str(&format!("{}  for (const f of {}.files) {{\n", ind, input_el));
                    js.push_str(&format!("{}    const buf = await f.arrayBuffer();\n", ind));
                    js.push_str(&format!("{}    files.push({{ name: f.name, size: f.size, mime: f.type, content: new Uint8Array(buf), last_modified: f.lastModified }});\n", ind));
                    js.push_str(&format!("{}  }}\n", ind));
                    if !multi_callback.is_empty() {
                        js.push_str(&format!("{}  ({})(files);\n", ind, multi_callback));
                    }
                } else {
                    js.push_str(&format!("{}  const f = {}.files[0];\n", ind, input_el));
                    js.push_str(&format!("{}  if (!f) return;\n", ind));
                    js.push_str(&format!("{}  const buf = await f.arrayBuffer();\n", ind));
                    js.push_str(&format!("{}  const fileData = {{ name: f.name, size: f.size, mime: f.type, content: new Uint8Array(buf), last_modified: f.lastModified }};\n", ind));
                    if !single_callback.is_empty() {
                        js.push_str(&format!("{}  ({})(fileData);\n", ind, single_callback));
                    }
                }
                js.push_str(&format!("{}}});\n", ind));
                js.push_str(&format!("{}{}.click();\n", ind, input_el));
                js.push_str(&format!("{}{}.appendChild({});\n", ind, parent, input_el));
                return;
            }

            let tag_js = js_tag(tag);
            js.push_str(&format!("{}const {} = document.createElement('{}');\n", ind, el, tag_js));
            gen_attrs(attrs, js, indent, &el);
            gen_events(attrs, js, indent, &el, model);

            // Link component: intercept clicks for client-side navigation
            if tag == &ComponentTag::Link {
                for a in attrs {
                    if a.name == "to" {
                        let target = match &a.value {
                            AttrValue::String(v) => format!("{:?}", v),
                            AttrValue::Expr(v) => v.clone(),
                            AttrValue::Flag => String::new(),
                        };
                        js.push_str(&format!("{}{}.addEventListener('click', (e) => {{ e.preventDefault(); navigate({}); }});\n", ind, el, target));
                    }
                }
            }

            js.push_str(&format!("{}A11yManager.applyAria({}, {:?});\n", ind, el, tag.as_str()));
            js.push_str(&format!("{}{}.appendChild({});\n", ind, parent, el));
        }
        UiNode::Slot { name, fallback } => {
            js.push_str(&format!("{}// Slot: {}\n", ind, name));
            if !fallback.is_empty() {
                for child in fallback {
                    gen_node(child, js, indent, parent, model);
                }
            }
        }
        UiNode::If { condition, then_branch, else_branch } => {
            js.push_str(&format!("{}if ({}) {{\n", ind, condition));
            for child in then_branch {
                gen_node(child, js, indent + 4, parent, model);
            }
            if !else_branch.is_empty() {
                js.push_str(&format!("{}}} else {{\n", ind));
                for child in else_branch {
                    gen_node(child, js, indent + 4, parent, model);
                }
            }
            js.push_str(&format!("{}}}\n", ind));
        }
        UiNode::For { item, list, body } => {
            js.push_str(&format!("{}for (const {} of {}) {{\n", ind, item, list));
            for child in body {
                gen_node(child, js, indent + 4, parent, model);
            }
            js.push_str(&format!("{}}}\n", ind));
        }
        UiNode::Match { expr, cases } => {
            js.push_str(&format!("{}switch ({}) {{\n", ind, expr));
            for case in cases {
                js.push_str(&format!("{}  case {}:\n", ind, case.pattern));
                for child in &case.body {
                    gen_node(child, js, indent + 8, parent, model);
                }
                js.push_str(&format!("{}    break;\n", ind));
            }
            js.push_str(&format!("{}}}\n", ind));
        }
        UiNode::Expr(e) => {
            js.push_str(&format!("{}// Expression: {}\n", ind, e));
            js.push_str(&format!("{}{}\n", ind, e));
        }
        UiNode::CodeBlock(code) => {
            let js_block = translate_kyle_block_to_js(code);
            if !js_block.is_empty() {
                js.push_str(&format!("{}// From inline Kyle block:\n", ind));
                for line in js_block.lines() {
                    js.push_str(&format!("{}{}\n", ind, line));
                }
            }
        }
        UiNode::Text(text) => {
            js.push_str(&format!("{}{}.appendChild(document.createTextNode({:?}));\n", ind, parent, text));
        }
    }
}

fn gen_attrs(attrs: &[UiAttr], js: &mut String, indent: usize, el: &str) {
    let ind = " ".repeat(indent + 2);
    for attr in attrs {
        // Check if this is a style-related attribute
        if let Some(css_prop) = style_attr_to_css(&attr.name) {
            match &attr.value {
                AttrValue::String(val) => {
                    let css_val = attr_style_value(&attr.name, val);
                    js.push_str(&format!("{}{}.style[{}] = {};\n", ind, el, css_prop, css_val));
                }
                AttrValue::Expr(expr) => {
                    let css_val = if is_simple_key(expr) {
                        format!("state.get({:?})", expr)
                    } else {
                        format!("{}", expr)
                    };
                    js.push_str(&format!("{}const __upd = () => {{ {}.style[{}] = {}; }};\n", ind, el, css_prop, css_val));
                    js.push_str(&format!("{}__upd();\n", ind));
                    // Watch count as a heuristic
                    js.push_str(&format!("{}state.watch('count', __upd);\n", ind));
                }
                AttrValue::Flag => {
                    js.push_str(&format!("{}{}.style[{}] = '1';\n", ind, el, css_prop));
                }
            }
            continue;
        }
        // Check for layout attribute (flexbox)
        if attr.name == "layout" {
            if let AttrValue::String(val) = &attr.value {
                let (ai, jc) = layout_to_flex(val);
                js.push_str(&format!("{}{}.style.display = 'flex';\n", ind, el));
                if !ai.is_empty() {
                    js.push_str(&format!("{}{}.style.alignItems = '{}';\n", ind, el, ai));
                }
                if !jc.is_empty() {
                    js.push_str(&format!("{}{}.style.justifyContent = '{}';\n", ind, el, jc));
                }
            }
            continue;
        }
        // Check for spacing attribute (gap in flexbox)
        if attr.name == "spacing" {
            if let AttrValue::String(val) = &attr.value {
                let v = if val.chars().all(|c| c.is_ascii_digit()) {
                    format!("{}px", val)
                } else { val.clone() };
                js.push_str(&format!("{}{}.style.gap = '{}';\n", ind, el, v));
            }
            continue;
        }
        // Regular attribute handling
        match &attr.value {
            AttrValue::String(val) => {
                match attr.name.as_str() {
                    "tpl" | "style" => {
                        js.push_str(&format!("{}applyStyle({}, {:?});\n", ind, el, val));
                    }
                    "animation" => {
                        js.push_str(&format!("{}applyAnimation({}, {:?});\n", ind, el, val));
                    }
                    "text" | "value" | "label" => {
                        js.push_str(&format!("{}{}.textContent = {:?};\n", ind, el, val));
                    }
                    "class" | "id" | "type" | "placeholder" | "src" | "alt" | "href" => {
                        js.push_str(&format!("{}{}.setAttribute({:?}, {:?});\n", ind, el, attr.name, val));
                    }
                    _ => {
                        // Unknown attr: try as style first, else setAttribute
                        let css_name = attr.name.replace("_", "-");
                        js.push_str(&format!("{}{}.style['{}'] = '{}';\n", ind, el, css_name, val));
                    }
                }
            }
            AttrValue::Expr(expr) => {
                match attr.name.as_str() {
                    "bind" => {
                        js.push_str(&format!("{}Binding.twoWay({}, state, {:?}, 'input');\n", ind, el, expr));
                    }
                    "text" | "value" => {
                        // For computed expressions, use watch-based approach
                        if is_simple_key(expr) {
                            js.push_str(&format!("{}Binding.oneWay({}, 'textContent', state, {:?});\n", ind, el, expr));
                        } else {
                            let js_expr = kyle_to_js_expr(expr);
                            js.push_str(&format!("{}const __upd = () => {{ {}.textContent = {}; }};\n", ind, el, js_expr));
                            js.push_str(&format!("{}__upd();\n", ind));
                            js.push_str(&format!("{}state.watch('count', __upd);\n", ind));
                        }
                    }
                    "tpl" => {
                        js.push_str(&format!("{}applyStyle({}, state.get({:?}));\n", ind, el, expr));
                    }
                    "animation" => {
                        js.push_str(&format!("{}applyAnimation({}, state.get({:?}));\n", ind, el, expr));
                    }
                    "checked" => {
                        js.push_str(&format!("{}Binding.oneWay({}, 'checked', state, {:?});\n", ind, el, expr));
                    }
                    "disabled" => {
                        js.push_str(&format!("{}Binding.oneWay({}, 'disabled', state, {:?});\n", ind, el, expr));
                    }
                    "click" | "change" | "input" | "mouse_enter" | "mouse_leave"
                    | "focus" | "blur" | "keydown" | "keyup" | "submit" | "scroll"
                    | "dblclick" | "context_menu" | "mouse_down" | "mouse_up"
                    | "mouse_move" | "touch_start" | "touch_end" | "touch_move" => {}
                    _ => {
                        let css_name = attr.name.replace("_", "-");
                        if is_simple_key(expr) {
                            js.push_str(&format!("{}{}.style['{}'] = state.get({:?});\n", ind, el, css_name, expr));
                        } else {
                            js.push_str(&format!("{}const __upd = () => {{ {}.style['{}'] = {}; }};\n", ind, el, css_name, expr));
                            js.push_str(&format!("{}__upd();\n", ind));
                        }
                    }
                }
            }
            AttrValue::Flag => {
                js.push_str(&format!("{}{}.setAttribute({:?}, '');\n", ind, el, attr.name));
            }
        }
    }
}

